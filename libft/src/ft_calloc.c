#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(t_size nmemb, t_size size)
{
	void	*ptr;

	if (nmemb * size == 0)
		return (malloc(0));
	ptr = malloc(nmemb * size);
	if (ptr == NULL)
		return (NULL);
	ft_bzero(ptr, (nmemb * size));
	return (ptr);
}
